--
-- Name: vue_capacite_hotel_header; Type: VIEW; Schema: public; Owner: amykayrala
--

CREATE VIEW public.vue_capacite_hotel_header AS
 SELECT chambre.id_hotel,
    count(chambre.num_chambre) AS nb_chambres_total,
    sum(chambre.capacite) AS capacite_totale
   FROM public.chambre
  GROUP BY chambre.id_hotel;


--
-- Name: vue_capacite_hotels; Type: VIEW; Schema: public; Owner: amykayrala
--

CREATE VIEW public.vue_capacite_hotels AS
 SELECT h.id_hotel,
    h.nom_hotel,
    count(c.num_chambre) AS nb_chambres_total,
    sum(c.capacite) AS capacite_totale
   FROM (public.hotel h
     LEFT JOIN public.chambre c ON ((h.id_hotel = c.id_hotel)))
  GROUP BY h.id_hotel, h.nom_hotel;

--
-- Name: vue_details_chambres; Type: VIEW; Schema: public; Owner: amykayrala
--

CREATE VIEW public.vue_details_chambres AS
 SELECT c.num_chambre,
    c.id_hotel,
    c.capacite,
    c.vue,
    c.prix,
    h.nom_hotel
   FROM (public.chambre c
     JOIN public.hotel h USING (id_hotel));


--
-- Name: vue_dispo_par_ville; Type: VIEW; Schema: public; Owner: amykayrala
--

CREATE VIEW public.vue_dispo_par_ville AS
 SELECT TRIM(BOTH FROM split_part((h.adresse)::text, ','::text, 2)) AS ville,
    count(c.num_chambre) AS total_dispo
   FROM (public.hotel h
     JOIN public.chambre c ON ((h.id_hotel = c.id_hotel)))
  WHERE ((c.disponibilite)::text = 'libre'::text)
  GROUP BY (TRIM(BOTH FROM split_part((h.adresse)::text, ','::text, 2)));


