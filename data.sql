CREATE FUNCTION public.set_room_free_on_delete() RETURNS trigger
    LANGUAGE plpgsql
    AS $$
BEGIN
    UPDATE chambre 
    SET disponibilite = 'libre' 
    WHERE num_chambre = OLD.num_chambre AND id_hotel = OLD.id_hotel;
    RETURN OLD;
END;
$$;


